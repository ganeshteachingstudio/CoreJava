package core.basics;

public class Variables {

    public static void main(String[] args) {

        // Variable declaration and initial value assignment (num1 is created and assigned value 30)
        int num1 = 30;

        // Printing the current value of num1
        System.out.println(num1);

        // Reassigning a new value (40) to the existing variable num1
        num1 = 40;

        // Printing the updated value of num1 after reassignment
        System.out.println(num1);
    }
}
